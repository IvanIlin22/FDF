#include "libft.h"

char		*ft_strchr(const char *s, int c)
{
	int		i;
	char	*n_s;

	i = 0;
	n_s = (char*)s;
	while (n_s[i] != c)
	{
		if (n_s[i] == '\0')
			return (NULL);
		i++;
	}
	return (&n_s[i]);
}
